	<h1>Suppléments alimentaires</h1>
	<p>Que ce soit dans un but de performance, de gain de masse, de perte de poids ou pour d’autres raisons, certaines personnes sont prêtes à consommer une panoplie de produits aux effets prétendus tous plus extraordinaires les uns que les autres. Il faut toutefois faire attention puisque l’efficacité de ces produits n’est pas toujours démontrée et certains peuvent même être néfastes pour la santé.</p>



	
	<h3>Qu’est-ce qu’un supplément alimentaire?</h3>
	
		<p>Un supplément alimentaire est un produit consommé, en plus de l’alimentation habituelle, dans l’objectif de combler une carence ou d’optimiser une qualité physiologique ou métabolique.</p>
		
		<h3>Les effets allégués des suppléments alimentaires</h3>
		<p>On parle d’allégations d’effets, puisque ces derniers sont rarement démontrés et que les études sur le sujet sont souvent mitigées. De plus, la réglementation visant à contrôler la qualité des produits est relativement récente et, pour le moment, peu d’entre eux ont reçu un numéro de produit naturel. D’ailleurs, ce numéro ne témoigne pas de l’efficacité du produit, mais plutôt du fait que la formulation, l’étiquetage et le mode d’emploi ont été approuvés par Santé Canada.</p>
		
		<h3>Les effets prouvés des suppléments alimentaires</h3>
		<p>À ce jour, il a été démontré que les glucides fournissent l’énergie nécessaire à l’effort, les glucides et les protéines favorisent la récupération postexercice et les boissons sportives remplacent les liquides et les électrolytes perdus à l’effort et aident à maintenir la glycémie (taux de sucre sanguin). Par contre, toutes ces substances font déjà partie de l’alimentation régulière. Les besoins en ces nutriments peuvent être comblés sans consommer de suppléments alimentaires.</p>
		
	<h3>Évaluer la nécessité d’un supplément</h3>
		<p>Avant de choisir d’utiliser ou non un supplément, il peut être utile de se poser certaines questions, comme par exemple :</p>
<ul>
	<li>Connaît-on tous les ingrédients contenus dans le produit?</li>
<li>Est-ce que le produit est sécuritaire pour la santé?</li>
<li>Est-il légal, ou encore, contient-il des substances interdites?</li>
<li>Les bénéfices du produit sont-ils supérieurs aux risques?</li>
</ul>	
<p>En fonction des réponses à ces questions, il sera possible de faire un choix éclairé qui aura des répercussions sur la performance sportive. Il ne faut donc pas négliger de bien s’informer sur le supplément avant de le consommer. Certains suppléments offrent des effets bénéfiques alors que d’autres peuvent représenter un danger. Dans le doute, et considérant qu’une alimentation équilibrée fournit généralement tous les nutriments nécessaires, il vaut mieux s’abstenir de consommer des suppléments.</p>
		
			
