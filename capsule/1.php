<p><strong>Adaptation/Surcharge : </strong>Pour susciter une adaptation, la charge doit être adéquatement choisie afin que la dernière répétition effectuée engendre une fatigue suffisante pour justifier le besoin d’un temps de repos.</p>

<p><strong>Volume vs Intensité : </strong>Il est important de bien équilibrer le volume (nombre de séries et de répétitions dans l’entrainement) avec l’intensité (charges soulevées); et ce, en fonction de l’objectif poursuivi. Plus l’intensité sera grande, plus le volume sera réduit. </p>

<p><strong>Temps de repos entre les séries : </strong>Selon l’objectif poursuivi, les temps de repos entre les séries varieront. Ils seront plus réduits pour des gains en endurance et de masse musculaire (hypertrophie) et plus longs pour des adaptations touchant davantage le système nerveux (puissance ou force maximale).</p>

<p><strong>Récupération entre les entrainements (fréquence d’entrainement) : </strong>Afin d’éviter le surentrainement et d’obtenir les adaptations voulues, il sera aussi important de s’assurer d’un temps de récupération adéquat entre deux entrainements.</p>

<p><strong>Variabilité : </strong>Il faudra modifier régulièrement la stimulation offerte par l’entrainement afin de susciter de nouvelles adaptations et d’éviter d’atteindre un plateau.</p>
