		<p>L’entrainement musculaire offre une multitude de méthodes et de façons adéquates d’atteindre des résultats. Suivant les objectifs et les besoins individuels, on peut se baser sur des principes d’entrainement généraux afin de s’entrainer de façon efficace :</p>
		
