	<h1>Contrôle du poids</h1>
	<p>Le surplus de poids est un problème de santé de plus en plus important, d’autant plus qu’il est un facteur de risque de nombreuses maladies chroniques. Que l’on parle d’embonpoint ou d’obésité, le surplus de poids est un problème complexe causé par des facteurs génétiques, les habitudes de vie (alimentation et activité physique), des problèmes psychologiques, des facteurs environnementaux, etc. Certains de ces facteurs, comme la génétique sont hors de notre contrôle. Par contre, en fournissant certains efforts et en exerçant une certaine vigilance, l’alimentation et l’activité physique peuvent être modulées selon le contexte et les individus. La «recette gagnante» pour la perte de poids combine d’ailleurs ces deux derniers facteurs.</p>
<p>Pour arriver à perdre du poids, il n’y a pas de secret : il s’agit d’avoir une balance énergétique négative, c’est-à-dire dépenser plus d’énergie par l’activité quotidienne que l’on en consomme par l’alimentation. Pour ce faire, il y a trois options : diminuer la quantité d’énergie consommée, augmenter la quantité d’énergie dépensée ou la combinaison des deux stratégies précédentes. Bien sûr, la meilleure stratégie est le plus souvent de combiner les deux options. En augmentant son niveau d’activité physique tout en contrôlant son apport alimentaire de façon progressive, on arrive souvent à de meilleurs résultats tout en se sentant plus en forme et en améliorant sa santé.
</p>


	
	<h3>Augmenter la dépense calorique</h3>
	
		<p>Pour augmenter la quantité d’énergie dépensée, il faut augmenter le niveau d’activité physique. Les exercices à dominance aérobie sont les plus efficaces pour augmenter la dépense calorique, par exemple la marche rapide, le jogging, la natation et la danse. Une séance d’activité physique à intensité élevée peut augmenter le métabolisme de repos pendant quelques heures, ce qui facilite la perte de tissu adipeux. Néanmoins, toute activité physique est valable et il faut en ajuster la nature, l’intensité et la durée selon les besoins de chaque individu. De plus, la musculation peut permettre de prévenir la diminution de la masse maigre (masse musculaire) et surtout de se sentir plus fort et plus en forme.</p>
		
		<h3>Diminuer l’apport calorique</h3>
		<p>Pour diminuer la quantité d’énergie consommée, il faut faire des choix d’aliments sains et consommer des portions dont la taille est adaptée à l’énergie dépensée. Les aliments devraient être choisis en fonction de leur valeur nutritive et énergétique. Une alimentation équilibrée et variée ciblant des aliments de tous les groupes du Guide alimentaire canadien est à préconiser. De plus, il est important d’être à l’écoute du signal de satiété de notre corps. Ce signal nous indique lorsque nous sommes rassasiés pour nous permettre de ne pas manger plus que nos besoins. Afin de permettre à ce signal de se manifester, il est préférable de prendre au moins 20 minutes pour manger.</p>
		
		<h3>Maintien</h3>
		<p>Après la perte de poids, l’étape du maintien est tout aussi importante, sinon plus. Le maintien du poids santé s’effectue en suivant la même logique adoptée pour la perte de poids, c’est-à-dire en ayant une dépense calorique égale à sa consommation. Par contre, le besoin énergétique peut être diminué suite à une perte de poids. Il est donc primordial d’être à l’écoute de son corps, de s’alimenter sainement et de continuer de pratiquer une activité physique régulièrement si l’on veut réussir à maintenir son poids.</p>
		
		
		
		
