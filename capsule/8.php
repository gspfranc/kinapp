	<h1>Qu’est-ce que la kinésiologie?</h1>
	<p>Le mot kinésiologie est formé des racines kinési, qui signifie mouvement, et logie, qui signifie science. La kinésiologie est donc la science du mouvement.</p>

<p>La Fédération des Kinésiologues du Québec définit la profession comme suit :</p>

<p><strong>« Le kinésiologue est le professionnel de la santé, spécialiste de l’activité physique, qui utilise le mouvement à des fins de prévention, de traitement et de performance.»</strong></p>

	
	<h3>Que fait un kinésiologue?</h3>
	
		<p>Le ou la  kinésiologue a pour objectif d’optimiser la performance motrice et ses déterminants de la condition physique, au moyen de l’activité physique. Il intervient avec la population en général, entre autres, auprès des jeunes, des personnes âgées, des femmes enceintes, ou encore auprès d’athlètes, dans une perspective de santé globale. Le kinésiologue peut travailler dans différents domaines selon ses champs de spécialisation. Après avoir évalué la condition physique d’un individu selon de nombreux paramètres, le kinésiologue est en mesure d’élaborer un programme d’activité physique spécifique et adapté aux besoins du client. Il en assure aussi le suivi afin d’y apporter les ajustements nécessaires pour favoriser le maintien et l’amélioration de la santé.</p>
		
		<p>Le kinésiologue joue un rôle de conseiller en matière d’activité physique auprès du client en recueillant de l’information et en l’orientant vers des choix pour favoriser de saines habitudes de vie. Le kinésiologue peut aussi œuvrer en tant qu’entraineur personnel ou encore en animant des cours de groupe comme de la danse, des cours en piscine ou des activités en plein air. Ses domaines d’activité comprennent la performance sportive, la santé et la sécurité au travail, la santé publique, l’intervention auprès de clientèles asymptomatiques et symptomatiques, l’entrainement personnel, la physiologie de l’exercice et la recherche. Le kinésiologue assure aussi la promotion de la santé et de l’activité physique par différents moyens. Il est également appelé à travailler en collaboration avec les autres professionnels de la santé.</p>
	

		<h3>Comment devenir kinésiologue?</h3>
		<p>Pour être kinésiologue, il faut compléter un baccalauréat en kinésiologie comprenant des cours théoriques et pratiques ainsi que des stages. La formation couvre plusieurs sujets, dont l’anatomie, la biomécanique du mouvement humain, l’apprentissage moteur, l’évaluation de la condition physique, les méthodes d’entrainement, la physiologie de l’exercice, la psychologie de l’activité physique et la nutrition. </p>
		
		
		
