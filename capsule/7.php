	<h1>Les étirements</h1>
	<h3>Pourquoi s’étirer?</h3>
	
		<p>Premièrement, pour le maintien de la flexibilité qui apporte des bienfaits notables :</p>
	<ul>
	<li>Diminuer les risques de blessures en augmentant la sensibilité proprioceptive</li>
	<li>Soigner certaines blessures ou douleurs chroniques</li>
	<li>Adopter une meilleure posture et faciliter les gestes de la vie courante</li>
	<li>Augmenter la prise de masse musculaire en améliorant l’efficacité des exercices de musculation (amplitude de mouvement plus grande)</li>
	</ul>

		<h3>Quand s’étirer?</h3>
		<ul><li>De 5 à 7 fois par semaine</li><li>À la fin de l’entraînement quand les muscles sont réchauffés</li></ul>
		
		
		<h3>Comment s’étirer?</h3>
		<ul>
		<li>Les exercices doivent faire sentir un étirement, mais aucune douleur</li>
		<li>La position d’étirement doit être maintenue de 20 à 30 secondes</li>
		<li>Chaque exercice doit être effectué de 2 à 3 fois</li>
		</ul>
		
		<h3>Pourquoi n’est-il pas souhaitable de s’étirer avant l’entraînement?</h3>
		<p>Les étirements procurent un effet de relaxation et de détente musculaire. Ils ne servent donc pas à préparer les muscles aux efforts déployés lors des exercices de musculation. En effet, les étirements statiques peuvent causer des microlésions musculaires, une diminution transitoire de la force et de la puissance ainsi qu’une diminution de la coordination intermusculaire. Les étirements en début de séance augmentent donc les risques de blessures.</p>
		
		<p>Pourtant, certains athlètes se doivent d’effectuer des étirements avant la pratique de leur sport. C’est le cas, notamment, des gymnastes, des patineurs artistiques, des contorsionnistes et des gardiens de but au hockey, puisque ces athlètes ont besoin de souplesse pour performer dans leur sport. On conseille à ceux-ci de favoriser les étirements dynamiques aux étirements statiques (par exemple ceux de type balistique décrit plus bas).</p> 

		<h3>Quelles sont les caractéristiques des différents types d’étirements?</h3>
		<h4>Étirements statiques</h4>
		<ul>
			<li>Efficacité moyenne</li>
			<li>Généralement passifs (sans contraction musculaire), parfois actifs</li>
			<li>Exercices simples et faciles à exécuter</li>
			<li>L’exercice consiste à maintenir une position d’étirement pendant un temps donné (autour de 30 secondes)</li>
			<li>Risque : activation du réflexe myotatique (contraction du muscle) si l’étirement est trop brusque</li>
		</ul>
		
		
		<h4>Étirements balistiques</h4>
		<ul>
			<li>Efficacité élevée à court terme, efficacité faible à long terme</li>
			<li>Actifs (avec contraction musculaire)</li>
			<li>L’exercice consiste à effectuer des mouvements pendulaires des membres</li>
			<li>Exercices appropriés pour les athlètes comme les sprinteurs, puisqu’ils préparent les muscles et les articulations à effectuer de grandes 
			<li>Amplitudes de mouvement sans nuire au réflexe myotatique</li>
			<li>Risque : contraction trop importante en réponse à l’étirement (réflexe myotatique)</li>

		</ul>
		
		<h4>Étirements PNF (proprioceptive neuromuscular facilitation)</h4>
		<ul>

			<li>Aussi appelés étirement «contracté-relâché»
			<li>Efficacité élevée
			<li>Passifs et actifs à la fois (avec contraction musculaire par moments)
			<li>L’exercice comprend trois phases successives, d’environ 6 secondes chacune :
			<ol>
			<li>Contraction statique dans une position où l’amplitude du mouvement est maximale</li>
			<li>Relâchement musculaire après la contraction</li>
			<li>Étirement dans une amplitude de mouvement plus grande qu’initialement</li></ol></li>
			<li>Désavantage : Parfois difficile à effectuer sans l’aide d’un partenaire</li>
		</ul>

		
