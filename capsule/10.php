	<h1>Prise de masse musculaire</h1>
	<p>Dans le but de développer une plus grande force musculaire, ou dans un but plutôt esthétique, l’augmentation de la masse musculaire est recherchée par plusieurs adeptes de la musculation. L’augmentation de la masse musculaire par le grossissement des fibres musculaires est appelée hypertrophie. L’entrainement en hypertrophie peut s’effectuer en plusieurs méthodes, qui impliquent les mêmes principes. Il s’agit d’avoir un volume d’entrainement relativement élevé et une charge correspondante, mais aussi une récupération incomplète. Ceci a pour but de susciter une fatigue musculaire pour stimuler le métabolisme des protéines qui restaurent les muscles. Voici quelques-unes des méthodes couramment utilisées pour développer l’hypertrophie :</p>



	
	<h3>Méthode traditionnelle</h3>
	
		<p>Cette méthode est généralement utilisée avec des appareils de musculation, mais peut aussi l’être avec des poids libres. Chaque exercice est exécuté pour un volume de 2 à 5 séries de 9 à 12 répétitions. Pour chaque groupe musculaire ciblé, on exécute entre 1 et 4 exercices. La récupération varie de 30 secondes à 2 minutes entre les séries et peut durer jusqu’à 5 minutes entre les exercices.</p>
		
		<h3>Méthode des « super-séries » agoniste-agoniste</h3>
		<p>Cette méthode jumelle deux exercices consécutifs sans temps de récupération pour le même groupe musculaire. Pour chaque exercice, de 2 à 5 séries de 6 à 12 répétitions sont effectuées. Le temps de récupération est de 1 à 2 minutes entre les super séries.</p>
		
		<h3>Méthode des « tri-séries »</h3>
		<p>Cette méthode est similaire à la précédente en y ajoutant un troisième exercice pour chaque groupe musculaire. Le temps de récupération est augmenté en conséquence et peut durer jusqu’à 3 minutes entre les tri-séries.</p>
		
	<h3>Méthode des « super-séries » agoniste-antagoniste</h3>
		<p>Contrairement aux précédentes méthodes, celle-ci fait intervenir des groupes musculaires opposés dans la super série. La prescription est identique : de 2 à 5 séries de 6 à 12 répétitions avec un temps de récupération de 1 à 2 minutes.</p>	
		
	<h3>Méthode pyramidale ascendante</h3>
		<p>Dans cette méthode, les charges utilisées varient d’une série à l’autre. Pour la première série, une charge permettant de faire jusqu’à 12 répétitions est utilisée. Ensuite, après un très court repos, la série suivante est entamée avec une charge plus lourde, qui devrait permettre d’effectuer un nombre maximal de répétitions inférieur à 12. La charge augmente donc à chaque série alors que le nombre maximal de répétitions diminue. Globalement, les séries doivent compter entre 6 et 12 répétitions, et chaque exercice devrait compter entre 2 et 5 séries. Entre les exercices, la récupération est d’une durée de 1 à 3 minutes.</p>
		
	<h3>Méthode pyramidale descendante</h3>
		<p>Inversement à la méthode pyramidale ascendante, la première série s’effectue cette fois avec la charge la plus lourde. La première série est donc exécutée avec une charge permettant un nombre maximal d’au moins 6 répétitions. La charge est ensuite diminuée et on effectue le maximum de répétitions possibles. (qui devrait se situer entre 1 et 5 répétitions). On exécute un nombre total de 2 à 5 séries par exercice, sans temps de repos entre les séries. La durée de la récupération entre les exercices est de 1 à 3 minutes.</p>	
		
		
